def test_language_detection():
    """Test language detection for all languages"""
    print("üß™ Comprehensive Language Detection Test")
    print("=" * 60)
    
    total_tests = 0
    total_correct = 0
    results = {}
    
    for lang_code, test_texts in TEST_DATA.items():
        print(f"\nüîç Testing {lang_code.upper()} ({len(test_texts)} samples)...")
        
        correct_count = 0
        lang_results = []
        
        for text in test_texts:
            try:
                response = requests.post(
                    "http://localhost:8000/detect-language",
                    headers={"Content-Type": "application/json"},
                    json={"text": text},
                    timeout=10
                )
                
                if response.status_code == 200:
                    result = response.json()
                    detected = result.get("detected_language", "unknown")
                    confidence = result.get("confidence", 0.0)
                    correct = detected == lang_code
                    
                    if correct:
                        correct_count += 1
                    
                    lang_results.append({
                        "text": text[:30] + "..." if len(text) > 30 else text,
                        "expected": lang_code,
                        "detected": detected,
                        "confidence": confidence,
                        "correct": correct
                    })
                else:
                    lang_results.append({
                        "text": text[:30] + "..." if len(text) > 30 else text,
                        "expected": lang_code,
                        "detected": "ERROR",
                        "confidence": 0.0,
                        "correct": False,
                        "error": f"HTTP {response.status_code}"
                    })
                
                time.sleep(0.1)  # Small delay
                
            except Exception as e:
                lang_results.append({
                    "text": text[:30] + "..." if len(text) > 30 else text,
                    "expected": lang_code,
                    "detected": "ERROR",
                    "confidence": 0.0,
                    "correct": False,
                    "error": str(e)
                })
        
        accuracy = (correct_count / len(test_texts)) * 100 if test_texts else 0
        results[lang_code] = {
            "total": len(test_texts),
            "correct": correct_count,
            "accuracy": accuracy,
            "results": lang_results
        }
        
        total_tests += len(test_texts)
        total_correct += correct_count
        
        # Print summary
        status = "‚úÖ" if accuracy >= 90 else "‚ö†Ô∏è" if accuracy >= 70 else "‚ùå"
        print(f"   {status} Accuracy: {accuracy:.1f}% ({correct_count}/{len(test_texts)})")
        
        # Show failed detections
        failed = [r for r in lang_results if not r["correct"]]
        if failed:
            print(f"   ‚ùå Failed detections:")
            for fail in failed[:2]:  # Show first 2 failures
                print(f"      Expected: {fail['expected']}, Got: {fail['detected']} (conf: {fail['confidence']:.2f})")
    
    # Overall summary
    overall_accuracy = (total_correct / total_tests) * 100 if total_tests > 0 else 0
    
    print("\n" + "=" * 60)
    print("üìä OVERALL RESULTS")
    print("=" * 60)
    print(f"Total Tests: {total_tests}")
    print(f"Correct Detections: {total_correct}")
    print(f"Overall Accuracy: {overall_accuracy:.1f}%")
    
    # Language-wise summary
    print(f"\nüìã LANGUAGE-WISE ACCURACY:")
    for lang_code, result in results.items():
        status = "‚úÖ" if result["accuracy"] >= 90 else "‚ö†Ô∏è" if result["accuracy"] >= 70 else "‚ùå"
        print(f"   {status} {lang_code.upper()}: {result['accuracy']:.1f}%")
    
    # Identify problematic languages
    problematic = [(lang, result) for lang, result in results.items() if result["accuracy"] < 90]
    if problematic:
        print(f"\n‚ö†Ô∏è  LANGUAGES NEEDING IMPROVEMENT (Accuracy < 90%):")
        for lang, result in problematic:
            print(f"   - {lang.upper()}: {result['accuracy']:.1f}%")
    
    # Save detailed results
    with open("comprehensive_test_results.json", "w", encoding="utf-8") as f:
        json.dump(results, f, indent=2, ensure_ascii=False)
    
    print(f"\nüíæ Detailed results saved to: comprehensive_test_results.json")
    
    return results

